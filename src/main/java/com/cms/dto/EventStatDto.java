package com.cms.dto;

public class EventStatDto {
    private long total;

    private long accelerate;

    private long decelerate;

    private long impact;

    private long turnLeft;

    private long turnRight;

    private long button;

    public long getTotal() {
        return total;
    }

    public void setTotal(long total) {
        this.total = total;
    }

    public long getAccelerate() {
        return accelerate;
    }

    public void setAccelerate(long accelerate) {
        this.accelerate = accelerate;
    }

    public long getDecelerate() {
        return decelerate;
    }

    public void setDecelerate(long decelerate) {
        this.decelerate = decelerate;
    }

    public long getImpact() {
        return impact;
    }

    public void setImpact(long impact) {
        this.impact = impact;
    }

    public long getTurnLeft() {
        return turnLeft;
    }

    public void setTurnLeft(long turnLeft) {
        this.turnLeft = turnLeft;
    }

    public long getTurnRight() {
        return turnRight;
    }

    public void setTurnRight(long turnRight) {
        this.turnRight = turnRight;
    }

    public long getButton() {
        return button;
    }

    public void setButton(long button) {
        this.button = button;
    }

    
}